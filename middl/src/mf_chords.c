#define xprintf(...)

static int chord_cmp(const void *a, const void *b)
{
  return strcmp(*(char**)a, *(char **)b) ;
}


static char crd_notes[32];

extern char mf_note_base[];

char *mf_getchord(char *name,char base,char octave,char transpose)
{
  char **q;
  int k=0;
  int pitch;
  int crd;
  char *notes;
  int note;

  char tmpname[20];

  crd_notes[0] = mf_chordend;
  while (charisspace(*name)) name++;
  xprintf("NAME:%s ",name); 
  if (*name=='-' || charisdigit(*name)) {
    do {
       crd_notes[k++] = transpose + (base+atoi(name) + 12 * octave);
       while (*name == '-') name++;
       while (charisdigit(*name)) name++;
       while (charisspace(*name) || *name == ',') name++;
    } while ((k<16) && (*name == '-' || charisdigit(*name)));
  }
  else {
    pitch = -1;
    if (lowercase(*name) == 'x') {
      pitch = base;
      name++;
    }
    else {
      pitch = lowercase(*name) - 'a';
      if ( 0 <= pitch && pitch <= 6) {
        pitch = mf_note_base[pitch];
        switch (*name++) {
          case '#' : pitch++; name++;break;  
          case 'b' : pitch--; name++;break;  
        }
      }
      else pitch = base;
      crd = -1;
      if (name[0] == 'm' && name[1] == '\0') crd = 73; /* min */
      else if (name[0] == 0) crd = 61; /* maj */  
      else if (name[0] > 0) {
        if (name[0] == ':') {
          strcpy(tmpname,"maj");
          strncpy(tmpname+3,name,16);
          tmpname[19]='\0';
          name = tmpname;
        }  
        q = bsearch(&name, chord_names, chord_max, sizeof(char *), chord_cmp);
        if (q != NULL)  crd = q - chord_names;
        xprintf("NAME3:%s NUM:%d ",name,crd); 
      }
      
      if (crd > 0) {
        xprintf("CRD:%s PITCH:%d OFF: %d LEN: %d = ",
                  chord_names[crd],pitch,chord_offset(crd),chord_length(crd)); 
        notes = chord_notes(crd);
        for (k=0; k< chord_length(crd); k++) {
          note = transpose + (pitch + *notes++) + 12 * octave;
          crd_notes[k] = note;
          xprintf("%d,",note);
        }
      }
    }
  }
  crd_notes[k] = mf_chordend;
  return crd_notes;
}




